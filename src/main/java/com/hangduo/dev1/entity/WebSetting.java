package com.hangduo.dev1.entity;

public class WebSetting {
    private Integer sid;
    private Integer web_status;
    private String web_title;
    private String web_dns;
    private String web_logo;
    private String semil;
    private String slanguage;
    private String cpy_info;
    private String agreement;

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public Integer getWeb_status() {
        return web_status;
    }

    public void setWeb_status(Integer web_status) {
        this.web_status = web_status;
    }

    public String getWeb_title() {
        return web_title;
    }

    public void setWeb_title(String web_title) {
        this.web_title = web_title;
    }

    public String getWeb_dns() {
        return web_dns;
    }

    public void setWeb_dns(String web_dns) {
        this.web_dns = web_dns;
    }

    public String getWeb_logo() {
        return web_logo;
    }

    public void setWeb_logo(String web_logo) {
        this.web_logo = web_logo;
    }

    public String getSemil() {
        return semil;
    }

    public void setSemil(String semil) {
        this.semil = semil;
    }

    public String getSlanguage() {
        return slanguage;
    }

    public void setSlanguage(String slanguage) {
        this.slanguage = slanguage;
    }

    public String getCpy_info() {
        return cpy_info;
    }

    public void setCpy_info(String cpy_info) {
        this.cpy_info = cpy_info;
    }

    public String getAgreement() {
        return agreement;
    }

    public void setAgreement(String agreement) {
        this.agreement = agreement;
    }
}
